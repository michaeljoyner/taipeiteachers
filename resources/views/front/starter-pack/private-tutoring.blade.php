        <h1 class="text-4xl mb-16 max-w-lg mx-auto text-site-secondary font-cursive">Private Tutoring</h1>
        <p class="mt-6 mb-2 text-xl"><span class="font-bold">$$$: </span>NT600 - 1200/hr</p>
        <p class="text-xl mb-2"><span class="font-bold">Pros: </span>Flexible times, better money, far less energy intensive.</p>
        <p class="text-xl mb-16"><span class="font-bold">Cons: </span>Frequent cancellations, more travel, and it is illegal for most people.</p>
        <h3 class="my-16 text-2xl font-bold">What is it?</h3>
        <p class="text-xl my-6">
            It’s important to know that private tutoring is illegal in Taiwan, unless you have the necessary paperwork. It’s illegal because you don’t have a work permit for it and because you’re not paying tax on it. But that being said, it’s still a fairly common practice by many teachers and plenty of websites and facebook groups are dedicated to helping Taiwanese locals find private tutors. If you work at a school you’re likely to be approached by a parent for some private tutoring for their children.
        </p>
        <p class="text-xl my-6">
            Private tutoring is a great way to bump your salary up a little. It’s up to you how many hours you’re willing to teach, when those hours will be, and you can always cancel the class if you feel it’s not going well.
        </p>
        <p class="text-xl my-6">
            It will be up to you how much you want to charge per hour. But take these factors into account when choosing your rate.
        </p>
        <ul class="text-xl pl-0 my-6">
            <li class="my-6">
                <p>
                    <span class="font-bold">Travel time: </span>
                    If it’s far from your work or home you’ll waste precious time traveling there and back. Take into account how much time you’ll spend commuting.
                </p>
            </li>
            <li class="my-6">
                <p>
                    <span class="font-bold">Preparation: </span>
                    If you’ll simply be following a book then you won’t need to do much preparation, but if you have to spend time preparing articles and worksheets and the likes, then remember to account for that too. See it as 1 hour for the class and 30 minutes for preparation at home and charge for what you think is fair for 90 minutes of your time. But then make sure you are always well prepared.
                </p>
            </li>
            <li class="my-6">
                <p>
                    <span class="font-bold">Expectations: </span>
                    This is linked to preparation in a way. If it’s just a parent who is looking for a tutor to help their child with his/her homework, then expectations won’t be incredibly high. But if it’s a parent who’s desperately trying to get their child into one of the notoriously tough international schools, they’ll be ready to pay more but you’ll be expected to deliver good results.
                </p>
            </li>
            <li class="my-6">
                <p>
                    <span class="font-bold">Location: </span>
                    The amount you can charge per hour also depends on the area. Generally speaking, people in New Taipei City won’t be prepared to pay over NT1000/hour unless you have a very good reputation, so NT800/hour would be a fair ask. However, in the more wealthy areas such as Tianmu and especially around the international schools (where school fees are exceptionally high) parents won't flinch at NT1000+ per hour, but they will have high expectations.
                </p>
            </li>
            <li class="my-6">
                <p>
                    <span class="font-bold">Number of students: </span>
                    Sometimes siblings, classmates, or just friends will team up for a private class. It works out well for both you and them as you can charge more for the hour while giving each student a discounted rate. For example, if you would have charged only one student NT800/hour, you could charge NT1200/h to teach both of them.
                </p>
            </li>
        </ul>
        <h3 class="my-16 text-2xl font-bold">What you should know.</h3>
        <p class="text-xl my-6">
            Private classes can be the easiest money you’ll ever make, or that part of your week that you dread from the moment you wake up on Monday morning. Having a student that wants to learn, is not shy to speak and takes part in the lesson will make the time fly by. You’ll feel great about the class and even better when you get your paycheck. On the other hand, having a conversation class with a super shy child, who’s been forced to take the class by their parents, can be like pulling teeth.
        </p>
        <p class="text-xl my-6">
            So here’s a pro tip for you. Offer the first class as a free test class to see if they like you and you like them. That’ll give you a chance to test the waters and gauge if you want to make it a regular thing or not.
        </p>

        <h3 class="my-16 text-2xl font-bold">How to get the job.</h3>
        <p class="text-xl my-6">
            You can also create a profile on <a href="http://myu.com.tw" class="text-site-primary">myu.com.tw</a>  and let people contact you, or you can check the tutoring jobs section on <a href="http://www.tealit.com" class="text-site-primary">tealit.com</a>. If you work at a school you might also be approached by parents to have private classes outside the school. Quite often tutoring jobs are passed on from other teachers who don’t the time to take on another student.
        </p>
        <p class="text-xl my-6">
            Word of mouth and reputation go a long way in the world of private tutoring, so do a good job and you’ll likely have more parents asking for private classes, and you’ll also be able to raise your hourly charge.
        </p>
