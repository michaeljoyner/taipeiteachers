        <h1 class="text-4xl mb-16 max-w-lg mx-auto text-site-secondary font-cursive">Adult Classes</h1>
        <p class="mt-6 text-xl mb-2"><span class="font-bold">$$$: </span>NT550 - 750/hr</p>
        <p class="text-xl mb-2"><span class="font-bold">Pros: </span>Mature conversation and material. Engaged learners, interesting students.</p>
        <p class="text-xl mb-16"><span class="font-bold">Cons: </span>More pressure, unstable hours, fewer jobs.</p>
        <h3 class="my-16 text-2xl font-bold">About the job.</h3>
        <p class="text-xl my-6">
            While there are far more jobs for teaching children, there are also some language centers that provide English classes to adults.
        </p>
        <p class="text-xl my-6">
            Students for these classes can range from university students intent on getting their English to a level where they can eventually work abroad, to workers who may have to use English at work, to housewives just looking to improve their conversation.
        </p>
        <p class="text-xl my-6">
            The upside of this kind of teaching is the students you’ll have and the material you'll be teaching. Firstly, students are there because they want to be there, as opposed to a child who has been sent to English class by his parents. So you’ll have a class full of students that are more engaged and willing to take part in the lesson, and being adults you’ll be able to communicate with them in mature way.
        </p>
        <p class="text-xl my-6">
            Secondly, the material you’ll teach will obviously be of a higher level, so you could find yourself discussing things like news stories, politics and opinions and even celebrity gossip.
        </p>
        <p class="text-xl my-6">
            On the downside, however, adults are busy and there schedules often change, so students come and go and often your working hours may do the same.
        </p>
        <p class="text-xl my-6">
            Due to this you may find it hard to get a good salary teaching adults alone. Many teachers who teach adult classes will take on a second teaching job or make additional money teaching private students.
        </p>

        <h3 class="my-16 text-2xl font-bold">What you should know.</h3>
        <p class="text-xl my-6">
            Compared to having to control a classroom full of energetic kids, teaching adults can seem like a much more peaceful and mature option. But unlike kids who are easily pleased with any sort of fun in the class, adults are there to learn, so you’ll need to be well prepared for every class.
        </p>
        <p class="text-xl my-6">
            Also, pleasing all the students in your class can be difficult as each one may have a different expectation of the class, and when one student leaves the class or doesn’t resign for the next semester, management  will be pointing fingers at you.
        </p>
        <p class="text-xl my-6">
            But don’t let that throw you off. Most Taiwanese are exceptionally friendly, especially in the classroom environment. If you’re a well spoken, knowledgeable person who can create, hold and direct conversations, teaching adults can be very rewarding with lots of opportunity to meet interesting Taiwanese locals and get fantastic insight into the culture.
        </p>
        <h3 class="my-16 text-2xl font-bold">How to get the job.</h3>
        <p class="text-xl my-6">
            Teaching adults is far more formal, so arrive at the interview looking sharp. A suit would be overkill, but long pants, closed shoes and a button up shirt at the least.
        </p>
        <p class="text-xl my-6">
            They will probably ask you to do a demo, however, it is unlikely that the demo will be in front of a real class. The manager may give you some of their materials and ask you to demonstrate how you would teach the material. On the other hand you could be asked to prepare your own material and demonstrate how you would teach that.
        </p>
        <p class="text-xl my-6">
            A lot of adult teaching is based around teaching articles, so perhaps print out an interesting article that is linked to a particular topic that you would present as your demo. Then have the important words and phrases that you would want the students to learn picked out already, as well as a few concept checking questions that you would ask. Discuss how you would present your article/topic, how you would get the students engaged or talking about it, how you would teach important words and phrases, and how you would make sure the students have a good understanding of what you have taught.
        </p>
