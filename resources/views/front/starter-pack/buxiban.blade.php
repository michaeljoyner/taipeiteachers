        <h3 class="my-16 text-2xl font-bold">About the job.</h3>
        <p class="text-xl my-6">
            A cram school is essentially a school that kids go to after they finish school. They’re also known as bixibans (roughly pronounced bu-she-ban). As most parents are still working when school finishes, most kids head to a nearby cram school, where they can do their homework and also take extra classes until their parents finish work.
        </p>
        <p class="text-xl my-6">
            Cram schools are privately owned, and many of them have built their entire business model around English, although they still do offer other classes such as math and science. Some of the most well known cram schools in Taiwan are HESS, Kid Castle, Kojen, and Joy.
        </p>
        <p class="text-xl my-6">
            Often cram schools will include a kindergarten and teachers will be required to do kindergarten in the morning and buxiban in the afternoon/evening.
        </p>
        <p class="text-xl my-6">
            Students are between the ages of 7 and 15 (elementary school and middle school), and working hours for these jobs fall roughly between 2pm and 10pm.
        </p>
        <p class="text-xl my-6">
            As a teacher, you’ll be teaching all the skills you’d expect to find in an English class, such as grammar, reading and comprehension, spelling and essay writing.
        </p>

        <h3 class="my-16 text-2xl font-bold">What you should know.</h3>
        <p class="text-xl my-6">
            If this is your first time teaching English as a second language, you might want to brush up on your grammar terms. If you’re not sure what the past participle or the present perfect continuous is, then it’s time for a quick crash course. It’s not rocket science, but as a native speaker you might not have been told the names for all the grammatical terms simply because we were never taught them. But to teach English as a second language you’ll need to be more familiar with them.
        </p>
        <p class="text-xl my-6">
            Also, learning how to best explain when and why we use different kinds of grammar is what makes a good ESL teacher. Grammar has never been an exciting thing to learn for anybody, so long explanations are only likely to put your students to sleep or confuse them even more. You need to be clear and to the point, so don’t be shy to get some pointers off the net before class. There are a ton of sites like <a href="http://busyteacher.org" class="text-site-primary">busyteacher.org</a> that offer great advice on how to teach particular grammar points.
        </p>
        <p class="text-xl my-6">
            These classes are also expected to be fun and entertaining, so use sites like <a href="https://www.teach-this.com" class="text-site-primary">teach-this.com</a>  to arm yourself with a few classroom games to make your class a bit more fun.
        </p>
        <p class="text-xl my-6">
            Also, be warned, teenagers are notoriously tough. Set the tone in the class from the very first lesson. If you’re too nice in the beginning you could find yourself with a class that is difficult to control later down the line.
        </p>
        <h3 class="my-16 text-2xl font-bold">How to get the job.</h3>
        <p class="text-xl my-6">
            Again, you’ll most likely have to do a demo. Establish beforehand the age of the kids and whether you can teach a topic of your choice or whether they want you to teach a page from one of the student books, often for cram schools it’s the latter.
        </p>
        <p class="text-xl my-6">
            The boss will be looking at how confident you are in the classroom, whether you can get the students speaking, and whether you can make the students smile and have a good time.
        </p>
        <p class="text-xl my-6">
            Demo any way you please, but a tried and tested formula is to begin with a brief introduction of yourself, spend a few minutes on an icebreaker game that gets the kids speaking and feeling relaxed, then teach the given material, and finally wrap it up with a quick review game that tests what they have just learned.
        </p>
        <p class="text-xl my-6">
            There are many classroom games that can be applied to almost anything you need to teach. Make sure you choose something fun that also doesn’t last too long. Some games take a long long time to complete themselves. Demos are best done as a short and sweet affair.
        </p>
