        <h1 class="text-4xl mb-16 max-w-lg mx-auto text-site-secondary font-cursive">Where to look for teaching jobs?</h1>
        <h3 class="text-2xl font-bold my-16">Tealit.com</h3>
        <p class="text-xl my-6">
            Tealit (short for teaching and living in Taiwan) has been around for years. The site serves as a sort of online classifieds where you can find jobs, apartments, language exchange partners and a whole lot more.
        </p>
        <p class="text-xl my-6">
            <a href="http://tealit.com" class="text-site-primary">Tealit.com</a> has job postings almost daily, So if you’re looking for a job in a hurry you should check in there a few times a day. However it serves the whole island of Taiwan, so finding a job in an area your hoping for can be a bit tough.
        </p>
        <p class="text-xl my-6">
            If you do see a job you’d like to apply for on Tealit.com you’ll need to call the number listed on the job post.
        </p>
        <p class="text-xl my-6">
            Remember to speak slowly and clearly. Not all the staff at English schools have fluent English, and even if they do, the speed at which native speakers speak can be difficult to understand. The polite thing to do is to speak slowly and clearly, get to the point, and use basic English that somebody speaking in their second language would understand. No slang and don’t ramble on!
        </p>
        <h3 class="text-2xl font-bold my-16">Facebook Groups</h3>
        <p class="text-xl my-6">
            There are a few groups on Facebook that have multiple posts every week (sometimes multiple posts a day) looking for teachers. These jobs are often for schools looking for a teacher in a hurry, so you’ll need to act fast (at least faster than the others). If you see a job that suits you - get in touch ASAP!
        </p>
        <p class="text-xl my-6">
            <a href="https://www.facebook.com/groups/233841013519/" class="text-site-primary">English Teacher in Taipei</a> has the most posts for teaching jobs in Taipei.
        </p>
        <p class="text-xl my-6">
            <a href="https://www.facebook.com/groups/TaiwanTeachingJobs/" class="text-site-primary">Taiwan English Teacher Job Openings</a> caters for the whole island with job being posted almost daily.
        </p>
        <h3 class="text-2xl font-bold my-16">Agents</h3>
        <p class="text-xl my-6">
            Going through an agent is certainly not necessary, but it does make things a lot easier. You can avoid the cold call, you can get information about the school before you go, your interview time will be set up, and they can give you all the info you need about the possibility of having to do a demo.
        </p>
        <p class="text-xl my-6">
            But it will come at a cost. And that’s where you should be careful because agents have different ways of charging.
        </p>
        <p class="text-xl my-6">
            One way is to charge a certain percent of your first paycheck. One particular agent charges 20% of your first paycheck, which is a fair ask.
        </p>
        <p class="text-xl my-6">
            The second method, and one that should be avoided at all costs, takes a small cut of your hourly wage. For example, they might find you a job for NT650/h and take NT25 of that as their fee. To many newcomers NT625/h sounds good enough, but if you stay with the same job for a while you’ll eventually regret the amount of money you’re missing out on each month.
        </p>
        <p class="text-xl my-6">
            Google will direct you to a number of agents operating in Taiwan, and nothing is stopping you from registering with all off them. Just check their terms of payment before agreeing to anything.
        </p>
        <p class="text-xl my-6">
            It’s also worth noting that agents only have a relationship with a certain amount of schools. That means there’s probably more jobs being advertised online than jobs that your agent knows about. So if you’re in a hurry to find a job it might be smart to sign up with your agent while also keeping an eye on Tealit.com and a few Facebook groups.
        </p>
